package dev.carbajal.stringmethods;

public class StringMethods {
	
	public static void main(String[] args) {
		
		String str = "Hello";
		
		// equals() method
		
		System.out.println(str.equals("Hello"));
		
		// length() method
		
		System.out.println(str.length());
		
		// indexOf() method
		
		System.out.println(str.indexOf("H"));
		
	}

}
